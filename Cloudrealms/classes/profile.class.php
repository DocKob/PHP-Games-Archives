class Profile{

	function display_blurb($id)
	{
		$getBlurb = mysql_query("SELECT blurb FROM users WHERE id = '$id'");
		while($row=mysql_fetch_array($getBlurb))
		{
			$blurb = $row[blurb];
		}
		return $blurb;
	}
}
